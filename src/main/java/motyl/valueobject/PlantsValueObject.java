package motyl.valueobject;

public class PlantsValueObject {
	
	private int id;
	private String name;
	private String lightNeccesity;
	private String img;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLightNeccesity() {
		return lightNeccesity;
	}
	public void setLightNeccesity(String lightNeccesity) {
		this.lightNeccesity = lightNeccesity;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	
	
}
