package motyl.valueobject;

public class AdultPhaseValueObject {
	
	private int id;
	private int adults;
	private int liberation;
	private int sanctuary;
	private int idLotePupa;
	private String commonNameSpecie;
	private String imgSpecie;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getAdults() {
		return adults;
	}
	public void setAdults(int adults) {
		this.adults = adults;
	}
	public int getLiberation() {
		return liberation;
	}
	public void setLiberation(int liberation) {
		this.liberation = liberation;
	}
	public int getSanctuary() {
		return sanctuary;
	}
	public void setSanctuary(int sanctuary) {
		this.sanctuary = sanctuary;
	}
	public int getIdLotePupa() {
		return idLotePupa;
	}
	public void setIdLotePupa(int idLotePupa) {
		this.idLotePupa = idLotePupa;
	}
	public String getCommonNameSpecie() {
		return commonNameSpecie;
	}
	public void setCommonNameSpecie(String commonNameSpecie) {
		this.commonNameSpecie = commonNameSpecie;
	}
	public String getImgSpecie() {
		return imgSpecie;
	}
	public void setImgSpecie(String imgSpecie) {
		this.imgSpecie = imgSpecie;
	}

	
}
